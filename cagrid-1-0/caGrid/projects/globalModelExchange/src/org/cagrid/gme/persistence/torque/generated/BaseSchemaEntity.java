      /** flag to prevent endless save loop, if this object is referenced
        by another object which falls in this transaction. */
    private boolean alreadyInSave = false;
      /**
     * Stores the object in the database.  If the object is new,
     * it inserts it; otherwise an update is performed.  This method
     * is meant to be used as part of a transaction, otherwise use
     * the save() method and the connection details will be handled
     * internally
     *
     * @param con
     * @throws TorqueException
     */
    public void save(Connection con) throws TorqueException
    {
          if (!alreadyInSave)
        {
            alreadyInSave = true;


  
            // If this object has been modified, then save it to the database.
            if (isModified())
            {
                if (isNew())
                {
                    SchemaEntityPeer.doInsert((SchemaEntity) this, con);
                    setNew(false);
                }
                else
                {
                    SchemaEntityPeer.doUpdate((SchemaEntity) this, con);
                }
                }

                                                
                                    if (collNamespaceImportMapsRelatedBySchemaId != null)
            {
                for (int i = 0; i < collNamespaceImportMapsRelatedBySchemaId.size(); i++)
                {
                    ((NamespaceImportMap) collNamespaceImportMapsRelatedBySchemaId.get(i)).save(con);
                }
            }
                                                            
                                    if (collNamespaceImportMapsRelatedByImportedSchemaId != null)
            {
                for (int i = 0; i < collNamespaceImportMapsRelatedByImportedSchemaId.size(); i++)
                {
                    ((NamespaceImportMap) collNamespaceImportMapsRelatedByImportedSchemaId.get(i)).save(con);
                }
            }
                                  alreadyInSave = false;
        }
      }

                        
